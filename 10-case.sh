ACTION=$1
case $ACTION in
    start)
         echo "XYZ service is starting"
         exit 0
         ;;
    stop)
         echo "XYZ service is stopping"
         exit 0
         ;;      
    restart)
         echo "XYZ service is restarting"
         exit 0
         ;;
     *)
         echo -e "\e[31m valid options are start or stop or restart only\e[0m"
         exit 1
 esac